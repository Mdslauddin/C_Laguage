#include<stdio.h>
#include<math.h>
int main()
{
    float a=2;
    printf("sin(2)=%f\n",sin(a));
    printf("cos(2)=%f\n",cos(a));
    printf("tan(2)=%f\n",tan(a));
    printf("exp(2)=%f\n",exp(a));
    printf("log10(2)=%f\n",log10(a));
    printf("sqrt(2)=%f\n",sqrt(4));
    printf("cbrt(2)=%f\n",cbrt(27));
    return 0;
}